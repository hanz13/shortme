const getInitialLanguage = (): LanguageCode => {
  if (typeof window !== "undefined") {
    const saved = localStorage.getItem("lang");
    if (saved) {
      return saved as LanguageCode;
    }
    
    const browserLangs = navigator.languages || [navigator.language];
    for (const lang of browserLangs) {
      if (!lang) continue;
      const lower = lang.toLowerCase();
      if (lower.startsWith("id") || lower.startsWith("ms")) return "ID";
      if (lower.startsWith("es")) return "ES";
      if (lower.startsWith("bg")) return "BG";
      if (lower.startsWith("bn")) return "BN";
      if (lower.startsWith("de")) return "DE";
      if (lower.startsWith("fr")) return "FR";
      if (lower.startsWith("he") || lower.startsWith("iw")) return "HE";
      if (lower.startsWith("hi")) return "HI";
      if (lower.startsWith("it")) return "IT";
      if (lower.startsWith("ja")) return "JA";
      if (lower.startsWith("pt")) return "PT";
      if (lower.startsWith("pl")) return "PL";
      if (lower.startsWith("ru")) return "RU";
      if (lower.startsWith("vi")) return "VI";
      if (lower.startsWith("tr")) return "TR";
      if (lower.startsWith("ko")) return "KO";
      if (lower.startsWith("zh-tw") || lower.startsWith("zh-hk") || lower.startsWith("zh-cht")) return "ZH_TW";
      if (lower.startsWith("ar")) return "AR";
    }
  }
  return "EN";
};

interface TranslationContextProps {
  language: LanguageCode;
  setLanguage: (lang: LanguageCode) => void;
  t: (key: string) => string;
}

const TranslationContext = createContext<TranslationContextProps | undefined>(undefined);

export const TranslationProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [language, setLanguageState] = useState<LanguageCode>(() => getInitialLanguage());

  const setLanguage = (lang: LanguageCode) => {
    setLanguageState(lang);
    localStorage.setItem("lang", lang);
  };

  // Safe fetch translation key fallback to EN
  const t = (key: string): string => {
    const dict = TRANSLATIONS[language] || TRANSLATIONS.EN;
    return dict[key] || TRANSLATIONS.EN[key] || key;
  };

  // Automatic GeoIP IP Language Detection
  useEffect(() => {
    const checkGeo = async () => {
      // Only auto-detect via IP lookup if user has not manually configured/saved a selection before
      if (localStorage.getItem("lang")) return;

      try {
        const res = await fetch("/api/geoip");
        if (res.ok) {
          const data = await res.json();
          if (data && data.success && data.country) {
            const countryCode = data.country.toUpperCase();
            // Map country codes to primary supported language codes in list
            const countryToLangMap: Record<string, LanguageCode> = {
              BG: "BG",
              BD: "BN",
              DE: "DE",
              AT: "DE",
              CH: "DE",
              ES: "ES",
              MX: "ES",
              CO: "ES",
              AR: "ES",
              FR: "FR",
              BE: "FR",
              IL: "HE",
              IN: "HI",
              ID: "ID",
              MY: "ID",
              SG: "ID",
              IT: "IT",
              JP: "JA",
              PT: "PT",
              BR: "PT",
              PL: "PL",
              RU: "RU",
              BY: "RU",
              KZ: "RU",
              VN: "VI",
              TR: "TR",
              KR: "KO",
              TW: "ZH_TW",
              HK: "ZH_TW",
              MO: "ZH_TW",
              AE: "AR",
              SA: "AR",
              EG: "AR",
              DZ: "AR",
              MA: "AR",
              IQ: "AR",
              JO: "AR",
              YE: "AR",
              SY: "AR",
              LB: "AR",
              OM: "AR",
              QA: "AR",
              KW: "AR",
              BH: "AR",
            };

            const matchedLang = countryToLangMap[countryCode];
            if (matchedLang) {
              setLanguageState(matchedLang); // Set without forcefully saving to localStorage to maintain auto-adaptability
            }
          }
        }
      } catch (err) {
        console.warn("Could not automatically translate via IP address detection", err);
      }
    };
    checkGeo();
  }, []);

  return (
    <TranslationContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </TranslationContext.Provider>
  );
};

export const useTranslation = () => {
  const ctx = useContext(TranslationContext);
  if (!ctx) {
    throw new Error("useTranslation must be used within a TranslationProvider");
  }
  return ctx;
};
